package jattask7;


		import java.util.ArrayList;

		public class RemoveAllElements {
		    public static void main(String[] args) {
		        // Creating an ArrayList of strings
		        ArrayList<String> list = new ArrayList<>();

		        // Adding some elements to the ArrayList
		        list.add("Apple");
		        list.add("Banana");
		        list.add("Cherry");
		        list.add("Date");

		        // Printing the original ArrayList
		        System.out.println("Original ArrayList: " + list);

		        // Removing all elements from the ArrayList
		        list.clear();

		        // Printing the ArrayList after removing all elements
		        System.out.println("ArrayList after clearing: " + list);
		    }
		


	}


